function place_character_3x5(x, y, character, pixel_width, pixel_height, color) {
    //"""Prints a 3x5 font using Playfield pixels, numbers upper case and some 
    //special characters"""
    var i,
        j,
        m,
        k = '',
        l = '',
        chartr = character.charCodeAt(0);
    if ((chartr < 32) || (chartr > 96)) {
        chartr = 63;
    }
    chartr -= 32;
    for (j = 0; j < 5; j += 1) {
        k = font_3x5[(chartr * 5) + j];
        for (i = 0; i < 3; i += 1) {
            l = k.charAt(i);
            if (l === 'X') {
                rectangle((x + i * pixel_width), (y + j * pixel_height), pixel_width, pixel_height, color);
            }
        }
    }
}



function String3x5(x, y, pstring, pixel_width, pixel_height, color)  {
    this.x = x;
    this.y = y;
    this.pstring = pstring;
    this.pixel_width = pixel_width;
    this.pixel_height = pixel_height;
    this.color = color;
}

// define Background draw method

String3x5.prototype.print = function() {
    //"""Prints a string that is left justified using 3x5 chracters in 
    //Player pixels."""
    var i,
        j;
    for (i = 0; i < this.pstring.length; i += 1) {
        j = this.pstring.charAt(i);
        place_character_3x5(this.x + (i * 4) * this.pixel_width, this.y, j, this.pixel_width, this.pixel_height, this.color);
    }
}






function Number3x5(x, y, value, pixel_width, pixel_height, color)  {
    this.x = x;
    this.y = y;
    this.value = value;
    this.pixel_width = pixel_width;
    this.pixel_height = pixel_height;
    this.color = color;
}

// define Background draw method

Number3x5.prototype.print = function() {
    //"""Prints a numeric value that is right justified using 3x5 digits in 
    //Player pixels."""
    var i,
        j,
        pstring;
    if (this.value < 0) {
        this.value -= this.value;
    }
    pstring = this.value.toString();
    for (i = 0; i < pstring.length; i += 1) {
        j = pstring.charAt(pstring.length - 1 - i);
        place_character_3x5(this.x - (i * 4) * this.pixel_width, this.y, j, this.pixel_width, this.pixel_height, this.color);
    }
}


